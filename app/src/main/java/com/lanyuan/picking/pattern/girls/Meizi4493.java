    @Override
    public Map<ContentsActivity.parameter, Object> getContent(String baseUrl, String currentUrl, byte[] result, Map<ContentsActivity.parameter, Object> resultMap) throws UnsupportedEncodingException {
        List<AlbumInfo> urls = new ArrayList<>();
        Document document = Jsoup.parse(new String(result, "gb2312"));
        Elements elements = document.select(".piclist li");
        for (Element element : elements) {
            AlbumInfo temp = new AlbumInfo();

            Elements title = element.select("a:has(img) span");
            if (title.size() > 0)
                temp.setTitle(title.get(0).text());

            Elements time = element.select(".b1");
            if (time.size() > 0)
                temp.setTime(time.get(0).text());

            Elements album = element.select("a:has(img)");
            temp.setAlbumUrl(baseUrl + album.attr("href"));
            Elements pic = album.select("img");
            if (pic.size() > 0)
                temp.setPicUrl(pic.get(0).attr("src"));
            urls.add(temp);
        }
        resultMap.put(ContentsActivity.parameter.CURRENT_URL, currentUrl);
        resultMap.put(ContentsActivity.parameter.RESULT, urls);
        return resultMap;
    }

    @Override
    public String getContentNext(String baseUrl, String currentUrl, byte[] result) throws UnsupportedEncodingException {
        Document document = Jsoup.parse(new String(result, "gb2312"));
        Elements elements = document.select("div.page a:containsOwn(下一页),div.page a:containsOwn(>)");
        if (elements.size() > 0) {
            if (elements.size() > 0) {
                Pattern pattern = Pattern.compile("http.*/");
                Matcher matcher = pattern.matcher(currentUrl);
                if (matcher.find()) {
                    return matcher.group() + elements.get(0).attr("href");
                }
            }
        }
        return "";
    }

    @Override
    public Map<DetailActivity.parameter, Object> getDetailContent(String baseUrl, String currentUrl, byte[] result, Map<DetailActivity.parameter, Object> resultMap) throws UnsupportedEncodingException {
        List<PicInfo> urls = new ArrayList<>();
        Document document = Jsoup.parse(new String(result, "gb2312"));
        PicInfo info = new PicInfo();
        Elements elements = document.select("div.picsbox p img");
        if (elements.size() > 0)
            info.setPicUrl(elements.get(0).attr("src"));
        Elements title = document.select(".picmainer h1");
        if (title.size() > 0)
            info.setTitle(title.text());
        Elements tags = document.select(".pleft a");
        if (tags.size() > 0) {
            List<String> tagList = new ArrayList<>();
            for (Element element : tags)
                tagList.add(element.text());
            info.setTags(tagList);
        }
        urls.add(info);
        resultMap.put(DetailActivity.parameter.CURRENT_URL, currentUrl);
        resultMap.put(DetailActivity.parameter.RESULT, urls);
        return resultMap;
    }

    @Override
    public String getDetailNext(String baseUrl, String currentUrl, byte[] result) throws UnsupportedEncodingException {
        Document document = Jsoup.parse(new String(result, "gb2312"));
        Elements elements = document.select("div.page a#nextpage");
        Elements elements1 = document.select("div.page a.current");
        if (elements.size() > 0 && elements1.size() > 0) {
            String current = elements1.text();
            Pattern pattern = Pattern.compile("http.*/");
            Matcher matcher = pattern.matcher(currentUrl);
            if (matcher.find()) {
                String href = elements.get(0).attr("href");
                Pattern pattern2 = Pattern.compile("[1-9]\\d*");
                Matcher matcher2 = pattern2.matcher(href);
                if (matcher2.find()) {
                    String next = matcher2.group();
                    if (current.equals(next))
                        return "";
                    return matcher.group() + next + ".htm";
                }
            }
        }
        return "";
    }

